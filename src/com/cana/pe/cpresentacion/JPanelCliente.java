    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
           
                cl.setDNI(txtDni.getText());
                cl.setNombre(txtNombre.getText());
                cl.setTelefeno(txtTelefono.getText());
                cl.setDireccion(txtDireccion.getText());
                clbo.agregarCliente(cl);
                listarCliente(); 
                JOptionPane.showMessageDialog(null,": Se guardo correctamente");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: al guardar Cliente");
        }
    }
         public boolean valida(){
        if(txtDni.getText().equals("")){
            JOptionPane.showMessageDialog(null,"DNI es requerido!");
            return false;
        }else if(txtNombre.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Nombre es requerido!");
           return false;
        }else if(txtTelefono.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Telefono es requerido!");
           return false;
        }else if(txtDireccion.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Direccion es requerido!");
           return false;
        }
        return true;
    
    }//GEN-LAST:event_btnGuardarActionPerformed

