    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {
            if(valida()){
                cl.setId_cliente(Integer.parseInt(idCliente));
                cl.setDNI(txtDni.getText());
                cl.setNombre(txtNombre.getText());
                cl.setTelefeno(txtTelefono.getText());
                cl.setDireccion(txtDireccion.getText());
                clbo.modificarCliente(cl);
                listarCliente();
                JOptionPane.showMessageDialog(null,":)se guardo correctamente");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, ":Error: al guardar Cliente ");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            Cliente cl = new Cliente();
            cl.setId_cliente(Integer.parseInt(idCliente));
            clbo.eliminarCliente(cl);
            listarCliente();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"ERROR" + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tablaClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaClienteMouseClicked
        int selec = tablaCliente.rowAtPoint(evt.getPoint());
        idCliente = tablaCliente.getValueAt(selec,0)+"";
         txtDni.setText(tablaCliente.getValueAt(selec,1)+"");
         txtNombre.setText(tablaCliente.getValueAt(selec,2)+"");
         txtTelefono.setText(tablaCliente.getValueAt(selec,3)+"");
         txtDireccion.setText(tablaCliente.getValueAt(selec,4)+"");
         
    }//GEN-LAST:event_tablaClienteMouseClicked
   
