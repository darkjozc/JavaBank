
package com.cana.pe.cmodelo;


public class Compania {
    private int id_compañia;
    private String Nombre;
    private int NumeroCompañia;

    public int getId_compañia() {
        return id_compañia;
    }

    public void setId_compañia(int id_compañia) {
        this.id_compañia = id_compañia;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    public int getNumeroCompañia() {
        return NumeroCompañia;
    }

    public void setNumeroCompañia(int NumeroCompañia) {
        this.NumeroCompañia = NumeroCompañia;
    }
    
}
